export const randomNames = () => {
    return randomEl(adjectives).replace(/^\w/, c => c.toUpperCase()) + ' ' + randomEl(nouns).replace(/^\w/, c => c.toUpperCase());
};

function randomEl(list) {
    const i = Math.floor(Math.random() * list.length);
    return list[i];
}

